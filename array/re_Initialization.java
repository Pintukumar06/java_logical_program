package array;

public class re_Initialization {
	public static void main(String[] args) {
		int balance=10000;
		int amount=5000;
		System.out.println(amount);
		System.out.println(balance);
		balance=balance+amount;
		amount=balance-amount;
		System.out.println(amount);
		System.out.println(balance);
	}
}